package model;

/**
 *
 * @author truong
 */
public class Users {
    public String user;
    public String pass;
    public String role;
    public String masv;

    public Users(String user, String pass, String role,String masv) {
        this.user = user;
        this.pass = pass;
        this.role = role;
        this.masv = masv;
    }

    public String getMasv() {
        return masv;
    }

    public void setMasv(String masv) {
        this.masv = masv;
    }

    public Users() {
    }
    

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
    
    
}
