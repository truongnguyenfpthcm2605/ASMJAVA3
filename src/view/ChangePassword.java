    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(checkForm()){
            try {
                us = UserDao.checkLogin(txtUser.getText(), txtOldPass.getText());
                if(us==null){
                    JOptionPane.showMessageDialog(this, "account is not exist");
                    return;
                }else{
                    us.setUser(txtUser.getText());
                    us.setPass(txtNewPass.getText());
                    UserDao.Update(us);
                    JOptionPane.showMessageDialog(this, "Update password successfull");
                    reset();
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

