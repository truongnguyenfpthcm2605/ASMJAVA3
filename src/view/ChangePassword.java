      private void reset() {
        txtUser.setText("");
        txtOldPass.setText("");
        txtNewPass.setText("");

    }

    private boolean checkValiDate(JTextField a) {
        if (a.getText().isEmpty()) {
            a.setBackground(Color.YELLOW);
            return false;
        } else {
            a.setBackground(Color.white);
        }
        return true;

    }

    public boolean checkForm() {
        if (!checkValiDate(txtUser)) {
            JOptionPane.showMessageDialog(this, "User is not empty ");
            return false;
        } else if (!checkValiDate(txtOldPass)) {
            JOptionPane.showMessageDialog(this, "Old pass is not empty ");
            return false;
        } else if (!checkValiDate(txtNewPass)) {
            JOptionPane.showMessageDialog(this, "New pass  is not empty");
            return false;
        }else if(!data.checkUSerOrPasss(txtNewPass.getText())){
            JOptionPane.showMessageDialog(this, "New pass wrong Format , New pass less than 10 charactor");
            txtNewPass.setBackground(Color.yellow);
            return false;
        }else{
            txtNewPass.setBackground(Color.white);
        }
        return true;
    }

